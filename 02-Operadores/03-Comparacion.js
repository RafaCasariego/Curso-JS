let a = 10;

console.log(a > 5); // Mayor que
console.log(a < 5); // Menor que
console.log(a >= 10); // Mayor o igual que
console.log(a <= 10); // Menor o igual que
console.log(a == 10); // Igual que
console.log(a != 10); // Diferente que
console.log(a === 10); // Estrictamente igual que
console.log(a !== 10); // Estrictamente diferente que
